# Creating Variables
x = 5
y = "John"
print(x)
print(y)

x = 4        # x is type int
x = "Sally"  # x is type str
print(x)

# Casting
x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0
print(x)
print(y)
print(z)

# Get the Type
x = 5
y = "John"
print(type(x))
print(type(y))

# Single or Double Quotes?
x = "John"
print(x)
# double quotes are the same as single quotes:
x = 'John'
print(x)

# Case-Sensitive
a = 4
A = "Sally"
print(a)
print(A)
